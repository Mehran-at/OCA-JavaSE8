package com.oca.operator.eight;

class Main {

    public static void main(String[] args) {

        System.out.println(23 / 2.0);
        System.out.println(23 % 2.0);
    }
}
/*
    As double point numbers are used in the expression, hence result should be in floating point number.

    Correct result is:

    23 / 2.0 = 11.5

    23 % 2.0 = 1.0
 */