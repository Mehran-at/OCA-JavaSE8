package com.oca.datatypesoperatorsvariables.fiftyfour;

class Article {

    private String name;
    private String author;

    Article() {

    }

    Article(String name, String author) {

        this.name = name;
        author = author;
    }

    String getName() {

        return name;
    }

    String getAuthor() {

        return author;
    }
}
